handle(Method, _, #{})
  when Method =:= <<"GET">>;
       Method =:= <<"POST">>;
       Method =:= <<"PUT">>;
       Method =:= <<"PATCH">>;
       Method =:= <<"DELETE">> ->
    {response, {200, <<"OK">>}, #{}, <<"OK">>};
handle(_, _, _) ->
    {response, {405, <<"Method Not Allowed">>}, #{}, <<"Method Not Allowed">>}.
